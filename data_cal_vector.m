function cal  = data_cal_vector(T,D)

a_1 = D(:,:,1);
a_2 = D(:,:,2);
a_3 = D(:,:,3);
R_u = 8.3144 ;

cal(1,:) = sigmoid(-T+1000) * R_u * (a_1(:,1)*T^(-2) + a_1(:,2)*T^(-1)+ a_1(:,3) + a_1(:,4)*T + a_1(:,5)*T^2 +a_1(:,6)*T^3 + a_1(:,7)*T^4)+...
      (sigmoid(-T+6000)+sigmoid(T-1000)-1) * R_u * (a_2(:,1)*T^(-2) + a_2(:,2)*T^(-1)+ a_2(:,3) + a_2(:,4)*T + a_2(:,5)*T^2 +a_2(:,6)*T^3 + a_2(:,7)*T^4)+...
      sigmoid(T-6000) * R_u * (a_3(:,1)*T^(-2) + a_3(:,2)*T^(-1)+ a_3(:,3) + a_3(:,4)*T + a_3(:,5)*T^2 +a_3(:,6)*T^3 + a_3(:,7)*T^4);

cal(2,:) = sigmoid(-T+1000) * R_u * (- a_1(:,1)*T^(-1) + a_1(:,2)*log(T)+ a_1(:,3)*T + a_1(:,4)*T^2 /2 + a_1(:,5)*T^3 /3 +a_1(:,6)*T^4 /4 + a_1(:,7)*T^5 /5 + a_1(:,8))+...
      (sigmoid(-T+6000)+sigmoid(T-1000)-1) * R_u * (- a_2(:,1)*T^(-1) + a_2(:,2)*log(T)+ a_2(:,3)*T + a_2(:,4)*T^2 /2 + a_2(:,5)*T^3 /3 +a_2(:,6)*T^4 /4 + a_2(:,7)*T^5 /5 + a_2(:,8))+...  
      sigmoid(T-6000) * R_u * (- a_3(:,1)*T^(-1) + a_3(:,2)*log(T)+ a_3(:,3)*T + a_3(:,4)*T^2 /2 + a_3(:,5)*T^3 /3 +a_3(:,6)*T^4 /4 + a_3(:,7)*T^5 /5 + a_3(:,8));

cal(3,:) = sigmoid(-T+1000) * R_u * (- a_1(:,1)*T^(-2)/2 - a_1(:,2)/T+ a_1(:,3)*log(T) + a_1(:,4)*T + a_1(:,5)*T^2 /2 +a_1(:,6)*T^3 /3 + a_1(:,7)*T^4 /4 + a_1(:,9))+...
    (sigmoid(-T+6000)+sigmoid(T-1000)-1) * R_u * (- a_2(:,1)*T^(-2)/2 - a_2(:,2)/T+ a_2(:,3)*log(T) + a_2(:,4)*T + a_2(:,5)*T^2 /2 +a_2(:,6)*T^3 /3 + a_2(:,7)*T^4 /4 + a_2(:,9))+...
    sigmoid(T-6000) * R_u * (- a_3(:,1)*T^(-2)/2 - a_3(:,2)/T+ a_3(:,3)*log(T) + a_3(:,4)*T + a_3(:,5)*T^2 /2 +a_3(:,6)*T^3 /3 + a_3(:,7)*T^4 /4 + a_3(:,9));


end